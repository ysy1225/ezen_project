package com.company.matching.model;

public class UsersDTO {
	
	private int userNum;
	private String userName;
	private String userEmail;
	private String userPhone;
	private String userPw1;
	private String userPw2;
	private String userRegdate;
	private int userLevel;
	private int warningLevel;

	public int getUserNum() {
		return userNum;
	}
	public void setUserNum(int userNum) {
		this.userNum = userNum;
	}
	public String getUserName() {
		return userName;
	}
	public void setUserName(String userName) {
		this.userName = userName;
	}
	public String getUserEmail() {
		return userEmail;
	}
	public void setUserEmail(String userEmail) {
		this.userEmail = userEmail;
	}
	public String getUserPhone() {
		return userPhone;
	}
	public void setUserPhone(String userPhone) {
		this.userPhone = userPhone;
	}
	public String getUserPw1() {
		return userPw1;
	}
	public void setUserPw1(String userPw1) {
		this.userPw1 = userPw1;
	}
	public String getUserPw2() {
		return userPw2;
	}
	public void setUserPw2(String userPw2) {
		this.userPw2 = userPw2;
	}
	public String getUserRegdate() {
		return userRegdate;
	}
	public void setUserRegdate(String userRegdate) {
		this.userRegdate = userRegdate;
	}
	public int getUserLevel() {
		return userLevel;
	}
	public void setUserLevel(int userLevel) {
		this.userLevel = userLevel;
	}
	public int getWarningLevel() {
		return warningLevel;
	}
	public void setWarningLevel(int warningLevel) {
		this.warningLevel = warningLevel;
	}

}
